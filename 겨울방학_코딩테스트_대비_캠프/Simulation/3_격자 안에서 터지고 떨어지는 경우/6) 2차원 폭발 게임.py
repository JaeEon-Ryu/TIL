
n,m,k = map(int,input().split())

grid = [
    list(map(int,input().split()))
    for _ in range(n)
]

# 격자 터트리기
def grid_burst():

    while True:
        bursted = False

        for j in range(n):
            prev_num = grid[0][j]
            prev_index = 0
            cnt = 1

            for i in range(1,n):
                curr_num = grid[i][j]

                if prev_num == curr_num and prev_num != 0:
                    cnt += 1
                else:
                    # cnt m개 이상일 시 연속적으로 터트리기
                    if cnt >= m :
                        bursted = True
                        for _ in range(prev_index,i):
                            grid[_][j] = 0

                    # 개수 초기화
                    prev_num = grid[i][j]
                    prev_index = i
                    cnt = 1

            # 맨 마지막 숫자가 같은 숫자로 끝날 때
            if cnt >= m :
                bursted = True
                for _ in range(prev_index,n):
                    grid[_][j] = 0

        if bursted == False:
            break

# 격자 정리하기
def grid_arrange(grid):

    temp_grid = [
        [0 for _ in range(n)]
        for _ in range(n)
    ]

    for j in range(n):
        temp_index = n-1

        for i in range(n-1,-1,-1):
            if grid[i][j] != 0:
                temp_grid[temp_index][j] = grid[i][j]
                temp_index -= 1

    return temp_grid

# 격자 회전하기
def grid_rotate(grid):

    temp_grid = [
        [0 for _ in range(n)]
        for _ in range(n)
    ]

    temp_row = n-1
    for i in range(n):

        for j in range(n):
            temp_grid[j][i] = grid[temp_row][j]
        temp_row -= 1

    return temp_grid


# k번 반복
grid_burst()
grid = grid_arrange(grid)
for _ in range(k):
    grid = grid_rotate(grid)
    grid = grid_arrange(grid)
    grid_burst()
    grid = grid_arrange(grid)


# 남아 있는 숫자 세기
result = 0
for i in range(n):
    for j in range(n):
        if grid[i][j] != 0:
            result += 1

# 결과
print(result)


